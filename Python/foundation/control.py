# Control Structure

## Selective Structure
c = -1
### if statement
if c > 0:
    print('c > 0')
elif c == 0:
    print('c == 0')
else:
    print('c < 0')

## Loop Structure
### while statement
c = -5
while c < 0:
    print(c)
    c += 1
else:
    print(c)
### for statement
for k in range(4):
    print(k)